	/*****************************************************************
	 * CREATE
	 * 
	 * @param parentID
	 * @param bioMightMethods
	 ******************************************************************/
	public void create(int hierarchy, int LOD, String parentID, ArrayList<BioMightPropertyView> bioMightProperties, ArrayList<BioMightMethodView> bioMightMethods)
	{
		curlins = new ArrayList();
		
		setImage("images/Curlins.jpg");
		setImageHeight(300);
		setImageWidth(300);
				
		try {
			// Get the information from the database via the Enterprise Bean		
			System.out.println("Getting CurlinsInfo for ParentID: " + parentID);
			InitialContext ctx = new InitialContext();
			BioMightBeanLocal bioMightBean = (BioMightBeanLocal) ctx.lookup(Constants.BioMightBeanRef);
			//bioMightTransforms = bioMightBean.getComponents(Constants.CurlinRef, Constants.CurlinsRef+":0");
			bioMightTransforms = bioMightBean.getComponents(Constants.CurlinRef, parentID);   	
		}catch (Exception e) { 
			System.out.println("Exception Getting Components - Curlins");
			throw new ServerException("Remote Exception getComponents():", e); 	
		}
		
		// This is a collection, so set the parent of the children
		// to that of the aggregation object
		componentID = parentID;
		
		// Run through the collection of Curlins and build them into the model
		// In the Default case, we get two instances of the hip, one
		// positioned on the right and one positioned on the left
		ArrayList transforms = bioMightTransforms.getTransforms();
		System.out.println("Curlins NumTransforms: " + transforms.size());
		for (int i=0; i<transforms.size(); i++)
		{
			// Get the information for the hip we are creating
			BioMightTransform bioMightTransform = (BioMightTransform) transforms.get(i);
			System.out.println("Creating Curlin: " + bioMightTransform.getName() + "  " + bioMightTransform.getId());
			
			// Create an instance of the Curlin for each tranform specified for the organism
			Curlin curlin = new Curlin(bioMightTransform.getId(), bioMightMethods);		
			System.out.println("Curlin Created!: " + bioMightTransform.getName() + "  " + bioMightTransform.getId());
			curlins.add(curlin);
			initProperty(bioMightTransform.getName(), Constants.Curlin, Constants.CurlinRef, bioMightTransform.getId());		
			System.out.println("Add Curlin to Collection!: " + bioMightTransform.getName() + "  " + bioMightTransform.getId());
		}

		// Set up methods that will be available to the Curlins
		initMethods();
	}
			

	
